                <b>Estrutura, Composição e Funcionamento</b></br>

A Terceira Secção compreende:</br>
A Primeira Subsecção - Fiscalização Prévia;</br>
A Segunda Subsecção - Fiscalização Concomitante e Sucessiva.</br>
A Secção é constituída por doze juízes, distribuídos por decisão do Presidente do Tribunal Administrativo, em função do movimento processual, sendo um deles o presidente da Secção.</br>
              </p>
