             <p>
<b>Competências da Terceira Secção do Tribunal Administrativo</b>
             <br>
Compete à Subsecção de Fiscalização Prévia da Secção das Contas Públicas, através do visto, verificar a conformidade com as leis em vigor e o cabimento orçamental, dos seguintes actos praticados por órgãos de soberania ou seus titulares, pelo Primeiro-Ministro e por membros do Conselho de Ministros:</br>
 
·         os contratos, de qualquer natureza, celebrados por entidades sujeitas à jurisdição do Tribunal;</br>
·         as minutas dos contratos nos termos da legislação relativa à fiscalização prévia;</br>
·         as minutas de contratos de qualquer valor que venham a celebrar-se por escritura pública e cujos encargos tenham de ser satisfeitos no acto da sua celebração;</br>
·         as minutas de contratos de qualquer valor que venham a celebrar-se por escritura pública e cujos encargos tenham de ser satisfeitos no acto da sua celebração;</br>
·         os diplomas e despachos relativos à admissão de pessoal não vinculado à função pública, assim como todas as admissões em categorias de ingresso na administração pública.</br>
 
Compete à Subsecção da Fiscalização Concomitante e Sucessiva da Secção das Contas Públicas, no âmbito das receitas e das despesas públicas:</br>
·         proceder à fiscalização concomitante e sucessiva dos dinheiros públicos, no âmbito das competências conferidas por lei, incluindo a avaliação segundo critérios de economia, eficácia e eficiência;</br>
·         proceder à fiscalização da aplicação dos recursos financeiros obtidos através de empréstimos, subsídios, avales e donativos, no âmbito da administração pública central;</br>
·         apreciar e decidir os processos de prestação de contas das entidades sob sua jurisdição;</br>
·         conhecer dos recursos interpostos dos tribunais administrativos provinciais e da Cidade de Maputo.</br>
·         Outras competências nos termos da lei.
              </p>
