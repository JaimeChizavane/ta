·         Os órgãos locais representativos do Estado;</br>
·         As autarquias locais nos termos da lei;</br>
·         As empresas públicas e as sociedades de capitais exclusiva ou maioritariamente públicos;</br>
·         Os exactores, tesoureiros, recebedores, pagadores e mais responsáveis pela guarda ou administração de dinheiros públicos;</br>
·         Os responsáveis por contas relativas a material ou equipamento ou quaisquer entidades que geram ou beneficiem de receitas ou financiamento provenientes de organismos internacionais ou das entidades referidas nas alíneas anteriores, ou obtidos com intervenção destas, consubstanciados, nomeadamente em subsídios, empréstimos ou avales;</br>
·         Os conselhos administrativos ou comissões administrativas;</br>
·         Os administradores, gestores ou responsáveis por dinheiros públicos ou outros activos do Estado, seja qual for a sua designação, bem como pelos fundos provenientes do exterior, sob a forma de empréstimos, subsídios, donativos ou outra;</br>
·         As entidades a quem forem adjudicados, por qualquer forma, fundos do Estado; </br>
·         Outras entidades ou organismos nos termos da lei</br>
