            <p>
<b>Como se dirigir à Terceira Secção Tribunal Administrativo?</b>
             <br>

Submetendo  à  respectiva  contadoria  os  actos  e  contratos  sujeitos  à fiscalização do TA;</br>
Quanto aos recursos, os mesmos são interpostos mediante requerimento devidamente articulado, dirigido ao Presidente do TA, no qual devem ser expostas as razões de facto e de direito em que se fundamentam e formuladas conclusões, no prazo de dez dias, contado da notificação da decisão recorrida, observando-se o regime de dilação constante do Código de Processo Civil.</br>
 
<b>Intervenção do Ministério Público</b></br>
Compete ao Ministério Público:</br>
·         Defender a legalidade e promover a realização do interesse público.</br>
·         Representar o Estado nas acções em que este for parte.</br>
·         Cabe, ainda, ao Ministério Público representar ou defender os interesses de outras pessoas indicadas por lei.</br>
Sempre que, em determinado processo, houver incompatibilidade entre as  diversas   funções  atribuídas   ao   Ministério   Público,  estas   são desempenhadas por diferentes agentes, designados pelo Procurador- Geral da Republica.</br>
 
<b>Infracções e Responsabilidade Financeira</b>
Constitui infracção financeira típica:
·         a apresentação de documentos ou declarações falsas;
·         a execução do acto ou contrato sem prévia sujeição a visto ou após o conhecimento de recusa de visto;
·         a desconformidade substancial entre a minuta e o contrato;
·         a não liquidação, cobrança ou entrega nos cofres do Estado das receitas devidas;
·         a violação das normas sobre a elaboração e execução dos orçamentos bem como da assunção, autorização ou pagamento de despesa pública ou compromisso;
·         a não efectivação ou retenção indevida dos descontos legalmente obrigatórios a efectuar ao pessoal;
·         a falta injustificada de remessa de contas ao tribunal competente ou sua apresentação com deficiências tais que impossibilitem ou gravemente dificultem a sua verificação;
 
<b>Efectivação da Responsabilidade</b>
A responsabilidade financeira é feita pelo Tribunal Administrativo e, na falta, pelo tribunal comum, podendo ser garantida através do arresto contra os responsáveis por dinheiro e outros valores públicos.
